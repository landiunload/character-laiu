/** Создаёт пустое поле для добавления пользователем */
export function createField(label = 'Новое поле', type = 'textarea') {
  return field(label, type);
}

/** Создаёт пустой раздел для добавления пользователем */
export function createSection(title = 'Новый раздел') {
  return section('✦', title, [field('Поле', 'textarea')]);
}
