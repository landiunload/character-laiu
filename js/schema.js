/**
 * Схема анкеты по умолчанию.
 *
 * Единственная ответственность модуля — описывать, из каких разделов и полей
 * состоит новая анкета (принцип единственной ответственности).
 * Остальное приложение работает с любой структурой такого вида,
 * поэтому разделы и поля можно свободно добавлять и удалять (принцип открытости/закрытости).
 *
 * Типы полей:
 *   'text'     — однострочное поле
 *   'textarea' — многострочное поле
 *   'pair'     — два значения в строке (например, «Любит / Не любит»)
 */

import { uid } from './utils.js';

/** Создаёт поле анкеты */
function field(label, type = 'text', hint = '') {
  return { id: uid(), label, type, hint, value: type === 'pair' ? ['', ''] : '' };
}

/** Создаёт раздел анкеты */
function section(icon, title, fields, options = {}) {
  return {
    id: uid(),
    icon,
    title,
    hint: options.hint || '',
    columns: options.columns || null, // подписи колонок для полей типа 'pair'
    fields,
  };
}

